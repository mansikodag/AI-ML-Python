#list
characters=['a','b','c','m','l','n','s','q','p','k','d','f','g']
print(characters)#to print characters
print(characters[2:5])#to print characters at 2,5
characters.reverse()#to reverse the list
print(characters)
characters.reverse()
characters.append('h')#to append character in list(by def at end)
print(characters)
characters.insert(3,'i')#to insert character('i') at 3
print(characters)
characters.sort()#to sort the list
print(characters)
characters.remove('f')#to remove character from list
print(characters)
characters.pop()#remove and return character(by def end)
print(characters)
characters.clear()#to clear all the characters in the list
print(characters)

